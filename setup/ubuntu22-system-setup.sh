#!/usr/bin/env bash
set -euo pipefail

SCRIPT_NAME="ubuntu22-system-setup"
SCRIPT_VERSION="1.6.0"
APPLY=false
CONFIG_FILE=""

while [[ $# -gt 0 ]]; do
  case "$1" in
    --apply) APPLY=true; shift ;;
    --config)
      CONFIG_FILE="${2:-}"
      [[ -z "$CONFIG_FILE" ]] && { echo "ERROR: --config requires a file path"; exit 1; }
      shift 2
      ;;
    -h|--help)
      echo "Usage: sudo bash setup/ubuntu22-system-setup.sh [--apply] [--config /path/to/config.conf]"
      exit 0
      ;;
    *) echo "ERROR: Unknown option: $1"; exit 1 ;;
  esac
done

RUN_ID="$(date +%Y%m%d-%H%M%S)"
LOG_DIR="/root/system-setup-logs/$RUN_ID"
BACKUP_DIR="/root/system-setup-backups/$RUN_ID"
CONFIG_DIR="/root/system-setup-configs"
GENERATED_CONFIG_FILE="$CONFIG_DIR/$RUN_ID.conf"
SUMMARY_FILE="/root/server-baseline-summary.txt"
VERIFY_SCRIPT="/root/ubuntu22-verify.sh"

SWAP_FILE="/swapfile"
DEFAULT_SWAP_SIZE="20G"
LIMIT_PROFILE="recommended"
LIMIT_VALUE="500000"
NPROC_VALUE="500000"
FILE_MAX="2097152"
SERVER_HOSTNAME=""
SERVER_ROLE="general-vps"
SERVER_LOCATION="unknown"
ADMIN_USER="admin"
SWAP_SIZE=""
SWAP_SIZE_BYTES=""
SSH_KEY_OPTION="2"
ADMIN_PUBLIC_KEY=""
EXTRA_TCP_PORTS=""
EXTRA_UDP_PORTS=""
ALLOW_PASSWORDLESS_SUDO="no"
SET_ADMIN_PASSWORD="yes"
SET_ROOT_PASSWORD="yes"
LOCK_ROOT_PASSWORD="no"
RUN_SYSTEM_UPDATES="yes"
ENABLE_UNATTENDED_SECURITY_UPDATES="yes"
ENABLE_WIREGUARD="no"
WIREGUARD_MODE="install-only"
WIREGUARD_INTERFACE="wg0"
WIREGUARD_PORT="51820"
WIREGUARD_ADDRESS="10.100.0.1/24"
WIREGUARD_PEER_PUBLIC_KEY=""
WIREGUARD_PEER_ALLOWED_IPS=""
CONFIG_LOADED=false
SSH_CHANGED=false
ADMIN_LOCAL_PASSWORD_SELECTED="unknown"
ROOT_PASSWORD_CHANGE_SELECTED="unknown"

[[ $EUID -ne 0 ]] && { echo "Run as root: sudo bash $0 --apply"; exit 1; }
mkdir -p "$LOG_DIR" "$BACKUP_DIR" "$CONFIG_DIR"
exec > >(tee -a "$LOG_DIR/console.log") 2>&1

info(){ echo; echo ">>> $*"; }
ok(){ echo "OK: $*"; }
change(){ echo "CHANGE: $*"; }
warn(){ echo "WARNING: $*"; }
fail(){ echo "ERROR: $*"; exit 1; }
run(){ echo "+ $*"; $APPLY && eval "$@"; }
quote_value(){ printf '%q' "$1"; }

backup_file(){ local f="$1"; [[ -f "$f" ]] && run "cp -a '$f' '$BACKUP_DIR/'"; }
write_if_changed(){
  local f="$1" c="$2"
  if [[ -f "$f" ]] && cmp -s "$f" <(printf "%s\n" "$c"); then ok "$f already matches desired config"
  else
    change "Updating $f"; backup_file "$f"
    if $APPLY; then mkdir -p "$(dirname "$f")"; printf "%s\n" "$c" > "$f"; fi
  fi
}
ensure_package(){ local p="$1"; dpkg -s "$p" >/dev/null 2>&1 && ok "Package installed: $p" || run "apt install -y '$p'"; }
ensure_line(){
  local f="$1" l="$2"
  if grep -Fxq "$l" "$f" 2>/dev/null; then ok "$l exists in $f"
  else change "Adding line to $f: $l"; backup_file "$f"; run "echo '$l' >> '$f'"; fi
}
prompt_default(){ local p="$1" d="$2" r; read -rp "$p [$d]: " r; echo "${r:-$d}"; }
prompt_yes_no(){ local p="$1" d="$2" r; read -rp "$p [$d]: " r; r="${r:-$d}"; [[ "$r" =~ ^[Yy]$ ]]; }
bool_value(){ prompt_yes_no "$1" "$2" && echo yes || echo no; }
validate_number(){ [[ "$1" =~ ^[0-9]+$ ]] || fail "$2 must be a whole number"; }

